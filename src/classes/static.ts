class Maths {
  static add(a: number, b: number): number {
    return a + b;
  }

  static subtract(a: number, b: number): number {
    return a - b;
  }

  static multiply(a: number, b: number): number {
    return a * b;
  }
}

console.log(Maths.add(10, 5)); // Output: 15

console.log(Maths.subtract(10, 5)); // Output: 5

console.log(Maths.multiply(5, 5)); // Output: 25
